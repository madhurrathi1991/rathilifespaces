	   function modifyUrl(data){
			var str = '';
			var i = 0;
			$.each(data, function(a, b) {
				if(a != 'page_no'){		                
					var and = (i>0) ? '&' : '';
					str += and+a+'='+b;
					i++;
				}

			});

			return  window.location.protocol+'//'+window.location.hostname+window.location.pathname+'?'+str
	   }

	   function load_filter_data(data){

		if(csrf_token == ''){
			 csrf_token = '79affac9048d68aef8a3f60b12bac6b3';
			 $("#final_csrf_token").val(csrf_token);
		 }
		 $('.awards_img_slider').slick('unslick');
			$.ajax({
				url: 'https://www.chandakgroup.com/pages/awards_load_ajax_data',
				type: 'POST',
				dataType: 'JSON',
				data:  {
					'page_no': data['page_no'],
					'year': data['year'],
					'last_page': '1',
					'csrf_chandakgroup': $("#final_csrf_token").val()
				},
				beforeSend: function() {		                
				},
				success: function(result){
					if(result["status"] == true){
						if(result["data"]){
							if(result["page_no"] == 2){
							$("#data").html(result["data"]);
						}else{
							$("#data").append(result["data"]);
						}
							
							$(".awards_img_slider").slick({
								dots: false,
								infinite: true,
								speed: 600,
								slidesToShow: 1,
								slidesToScroll: 1,
								arrows: true,
								responsive: [
									{
										breakpoint: 769,
